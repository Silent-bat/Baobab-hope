function deepApply(obj, dict) {
    if (!obj || typeof obj !== 'object' || Array.isArray(obj)) {
        return typeof obj === 'string' && dict.hasOwnProperty(obj) ? dict[obj] : obj;
    }
    const result = {};
    for (const key in obj) {
        result[key] = deepApply(obj[key], dict);
    }
    return result;
}

